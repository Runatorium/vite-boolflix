<script>
import {store} from '../store.js'

export default{
    name: 'MySearchedShows',

    data() {
            return {
              store
            }
          },
          methods:{
          calcvote(vote){
            let nstars = "";
            let voto = Math.ceil(vote / 2).toFixed(1) ;
            for(let index=0; index<5; index++){
                if(index < voto){
                    nstars += '<i class="fa-solid fa-star"></i>'
                }else{
                    nstars += '<i class="fa-regular fa-star"></i>'
                } 
            }
            return nstars;
          },
          checklang(toCheck){
            
            if(toCheck == 'en'){
                toCheck = 'gb'
            }else if(toCheck == 'ja'){
                toCheck = 'jp'
            }
            return toCheck;
          },
          movieposter(urlToCheck){
            let finalUrl = "";
            if(urlToCheck == null){
               finalUrl += "../../public/standardlogo.png"
            }else{
                finalUrl = store.movieurl
                finalUrl += urlToCheck
                console.log(finalUrl)
            }
            return finalUrl;
          }
          
            
          }
}

</script>


<template>
    <div>
        <h2 v-if="store.searchedShows.length > 0">TV Shows</h2>
    </div>
    
    <div class="box">
            <div v-for="(show) in store.searchedShows" class="show">
                <img :src="movieposter(show.poster_path)" alt="">
                <h1>{{show.name}}</h1>
                <h3>{{show.original_name}}</h3>
                <span :class="store.standard + checklang(show.original_language)"></span>
                <h4 v-html="calcvote(show.vote_average)"></h4>
            </div>
    </div>

</template>

